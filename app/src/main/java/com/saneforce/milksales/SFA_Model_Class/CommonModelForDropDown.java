package com.saneforce.milksales.SFA_Model_Class;

public class CommonModelForDropDown {
    String id, title;

    public CommonModelForDropDown(String id, String title) {
        this.id = id;
        this.title = title;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
